package exercises;

/*
 * 8-  Make a Program that asks how much you earn per hour 
 * 	   and the number of hours worked in the month. 
 * 	   Calculate and show the total of your salary in that month.
 */

public class Exercise08 {

	public static double salaryCalculation(double hourValue, int monthHours) {
		return hourValue * monthHours;
	}

}
